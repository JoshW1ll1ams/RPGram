	// Search algorithm that searches the map and returns the indexes of specified block id's
	public static ArrayList<Integer> GetBlock(int[] map, int BlockID)
	{
		ArrayList<Integer> num = new ArrayList<Integer>();
		for(int i = 0; i < map.length; i++)
		{
			if(map[i] == BlockID) // If map index matches block id we add it to an array list 
			{
				num.add(Integer.valueOf(i));
			}
		}
		return num;
	}

	

	
			
